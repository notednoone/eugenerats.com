                    <li>Place traps perpendicular to walls where rats travel</li>
                    <li>Bait with peanut butter, bacon, or dried fruit</li>
                    <li>Use multiple traps (10-12 for serious infestations)</li>
                    <li>Check and reset traps daily</li>
                    <li>Wear gloves when handling traps and dead rats</li>
