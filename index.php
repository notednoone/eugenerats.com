        <div class="faq">
            <h2>Frequently Asked Questions</h2>
            
            <div class="faq-item">
                <div class="faq-question">How do I know if the rats on my property are actually rats and not mice or squirrels?</div>
                <div class="faq-answer">Signs include rat droppings that are larger than rice grains, burrow holes, runways in vegetation, gnaw marks, and nighttime noises in walls or attics.</div>
            </div>

            <div class="faq-item">
                <div class="faq-question">What are the most common places rats nest around homes and yards in Eugene?</div>
                <div class="faq-answer">Rats nest in overgrown vegetation, wood piles, compost bins, garbage areas, and under structures. Dense ground cover and stacked debris are major attractants.</div>
            </div>

            <div class="faq-item">
                <div class="faq-question">What DIY steps can I take right now to reduce food and shelter that attract rats?</div>
                <div class="faq-answer">Remove food sources (birdseed, pet food, fallen fruit, open compost), trim vegetation, store trash securely with tight lids, and keep outdoor feed contained (pet & livestock).</div>
            </div>

            <div class="faq-item">
                <div class="faq-question">What home-sealing methods can I use to keep rats out?</div>
                <div class="faq-answer">Seal gaps bigger than ¼″ around foundations, pipes, vents, doors, crawlspaces, and roofs. Use steel wool, hardware mesh, spray foam, or cement to block entry.</div>
            </div>

            <div class="faq-item">
                <div class="faq-question">What DIY trapping options work best (and how should I set them)?</div>
                <div class="faq-answer">Snap traps are inexpensive and effective if placed against walls or along rat travel paths; bait with peanut butter or nuts.</div>
            </div>

            <div class="faq-item">
                <div class="faq-question">Are poisons a good DIY option for rat control?</div>
                <div class="faq-answer">Poisons (rodenticides) are not recommended for indoor use, can harm pets and wildlife, and may cause dead rats to decompose inside walls. Use them only as a last resort and in secure bait stations.</div>
            </div>

            <div class="faq-item">
                <div class="faq-question">What safety precautions should I follow when cleaning up after rats?</div>
                <div class="faq-answer">Wear gloves, masks, and protective eyewear. Wet clean instead of vacuuming/sweeping to avoid airborne contaminants. Disinfect contaminated areas with a solution like 10:1 water:bleach.</div>
            </div>

            <div class="faq-item">
                <div class="faq-question">Why is rat control a community issue in Eugene and Lane County?</div>
                <div class="faq-answer">Rats are a public health risk and can spread disease; removal efforts by one household may be undermined if neighboring properties continue to attract rats.</div>
            </div>

            <div class="faq-item">
                <div class="faq-question">What should I do if my rat problem seems too big for DIY methods?</div>
                <div class="faq-answer">Consider contacting a professional pest control service — especially with heavy infestations or rats inside walls and attics.</div>
            </div>

            <div class="faq-item">
                <div class="faq-question">Who can I contact locally in Eugene for advice or reporting rats on public property or sewers?</div>
                <div class="faq-answer">Eugene Public Works and Code Compliance provide resources and can inspect sewer issues or public rat sightings.</div>
            </div>

            <div class="faq-item">
                <div class="faq-question">Are there local rules or guidelines about composting and rat control?</div>
                <div class="faq-answer">Keep compost in rodent-proof bins with tight lids, avoid open piles, and remove fallen fruit promptly — these attract rats and make control harder.</div>
            </div>

            <div class="faq-item">
                <div class="faq-question">Can neighbors help reduce rat problems?</div>
                <div class="faq-answer">Yes — working with neighbors to eliminate food sources and secure yards can reduce community-wide rodent pressure.</div>
            </div>
